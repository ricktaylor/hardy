#[test]
fn bib_different_target_set_detected() {
    // BIB_BUNDLE: BIB covers payload(1) and BundleAge(2) — two targets.
    // bib_one_target: same source (ipn:3.1), same payload HMAC, but BIB
    // covers only payload(1). Resolved target sets differ → not equivalent.
    // ASB: targets=[1], ctx_id=1, ctx_flags=0, source=ipn:3.1, results=[[result-1]]
    // (63 bytes total, bstr prefix 0x3F).
    let bib_one_target = hex!(
        "9F88070000820282010282028202018202820201820018281A000F4240"
        "850B030000583F"  // BIB block 3, bstr(63)
        "810101008202820301"  // targets=[1], ctx_id=1, ctx_flags=0, source=ipn:3.1
        "818182015830"  // results=[[result-for-1]], code=1, bstr(48)
        "F75FE4C37F76F046165855BD5FF72FBFD4E3A64B4695C40E2B787DA005AE819F0A2E30A2E8B325527DE8AEFB52E73D71"
        "850702000041008501010000"
        "5823526561647920746F2067656E657261746520612033322D62797465207061796C6F6164"
        "FF"
    );
    assert!(!eq(BIB_BUNDLE, &bib_one_target));
}

#[test]
fn bib_different_source_detected() {
    // BIB_BUNDLE has source ipn:3.1; change the source EID to ipn:4.1.
    // Source is at the head of the ASB: 82 01 02 → 82 01 02 (count), then
    // 00 (context), then 82 02 83 01 (ipn:3.1) → swap last byte 01 → 04
    // to get ipn:4.1 (82 02 83 04).
    let other_source = hex!(
        "9F88070000820282010282028202018202820201820018281A000F4240850B030000587582010201008202820401828182015830F75FE4C37F76F046165855BD5FF72FBFD4E3A64B4695C40E2B787DA005AE819F0A2E30A2E8B325527DE8AEFB52E73D7181820158306EE5CA30AB3A1BF1E7F645EB21418FFC129BACFB69677FDAE0D08CB63159358FA86BE682538299B4B7E53C04FE03FDE88507020000410085010100005823526561647920746F2067656E657261746520612033322D62797465207061796C6F6164FF"
    );
    assert!(!eq(BIB_BUNDLE, &other_source));
}

